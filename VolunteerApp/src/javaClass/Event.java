package javaClass;

public class Event {
	private int eventDetID;
	private String name;
	private int createdBy;
	private String eventDate;
	private String startTime;
	private String endTime;
	private int numberDays;
	private String img;
	private String details;
	private int eventLocID;
	private String county;
	private String location;
	private String userStart;
	private String userEnd;
	private int eventID;
	

	public int getEventDetID() {
		return eventDetID;
	}
	public void setEventDetID(int eventDetID) {
		this.eventDetID = eventDetID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(int createdBy) {
		this.createdBy = createdBy;
	}
	public String getEventDate() {
		return eventDate;
	}
	public void setEventDate(String eventDate) {
		this.eventDate = eventDate;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public int getNumberDays() {
		return numberDays;
	}
	public void setNumberDays(int numberDays) {
		this.numberDays = numberDays;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}

	public String getDetails() {
		return details;
	}
	public void setDetails(String details) {
		this.details = details;
	}
	public String getCounty() {
		return county;
	}
	public void setCounty(String county) {
		this.county = county;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getUserStart() {
		return userStart;
	}
	public void setUserStart(String userStart) {
		this.userStart = userStart;
	}
	public String getUserEnd() {
		return userEnd;
	}
	public void setUserEnd(String userEnd) {
		this.userEnd = userEnd;
	}
	public int getEventID() {
		return eventID;
	}
	public void setEventID(int eventID) {
		this.eventID = eventID;
	}
	
	public int getEventLocID() {
		return eventLocID;
	}
	
	public void setEventLocID(int eventLocID) {
		this.eventLocID = eventLocID;
	}
	
	
	
}
